class Empleado {
  String id;
  String nombreEmpleado;
  double salario;
  String puesto;
  String departamento;
  String direccion;
  String telefono;

  Empleado({
    required this.id,
    required this.nombreEmpleado,
    required this.salario,
    required this.puesto,
    required this.departamento,
    required this.direccion,
    required this.telefono,
  });
}
